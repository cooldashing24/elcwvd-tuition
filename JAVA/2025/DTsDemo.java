//PascalCase
// DataTypes
class DTsDemo
{
	public static void main(String[] args)
	{
		System.out.println("Hello");	// String	- double quotes
		System.out.println("");	// empty string
		
		System.out.println(77);	// int
		System.out.println(48989749786548478L);	// long
		
		System.out.println(3.14);	// double
		System.out.println(9.8F);	// float
		
		System.out.println('#');	// char - single quotes
		System.out.println(' ');	// char
		
		// 1 byte = 8 bits
		// 1 bit
		System.out.println(true);	// boolean
		System.out.println(false);	// boolean
	}
}
