class EscapeCharactersDemo
{
	public static void main(String args[])
	{
		System.out.println("Hello\t\tWorld");
		System.out.println("Hello\nWorld");
		
		System.out.println("Maru naam \"Vishnu\" 6e");
		
		char singleQuote = '\'';
		System.out.println("Single Quote Char: " + singleQuote + "Hello ");
	}
}