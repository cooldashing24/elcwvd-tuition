//PascalCase
// DataTypes
class FirstProgDemo
{
	public static void main(String[] args)
	{
		System.out.println("Hello");	// String
		System.out.println("-5");	// String
		System.out.println("");
		
		System.out.println('$');		// char
		System.out.println('#');		// char

		System.out.println(56);		// int	-> default
		System.out.println(-65L);		// long
		
		System.out.println(-3.14);		// double -> default
		System.out.println(9.8f);		// float
		
		System.out.println(true);		// boolean
		System.out.println(false);		// boolean
		
		// char -> 
	}
}
