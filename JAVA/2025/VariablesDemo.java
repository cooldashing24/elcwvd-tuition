class VariablesDemo
{
	public static void main(String[] args)
	{
		// Syntax for declaration
		// dataType variableName;

		double weight = 78.44;			// declaration & initialization
		char dollarSymbol;		// declaration
		dollarSymbol = '$';		// initialization
		System.out.println(dollarSymbol);
		
		int totalBooks;			// declaration
		totalBooks = 24;		// initialization
		System.out.println(totalBooks);
		
		System.out.println(weight);
		weight = 79.1;
		System.out.println(weight);
		
		float height = 12.5F;
		System.out.println(height);
		
		byte age = 12;
		System.out.println(age);
		
		long totalAnts = 48989749786548478L;
		System.out.println(totalAnts);
	}
}
