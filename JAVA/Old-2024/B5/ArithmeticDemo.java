//  + - * / %
class ArithmeticDemo
{
	public static void main(String[] args)
	{
		double x = 58, y = 57;
		
		System.out.println("Value of X: " + x);
		System.out.println("Value of Y: " + y);
		
		double sum = x + y;
		System.out.println("Sum of X & Y: " + sum);
		
		
		System.out.println("Add X + Y: " + x + y);
		System.out.println("Add (X + Y): " + (x + y));
		
		System.out.println("Difference (X - Y): " + (x - y));
		System.out.println("Product (X * Y): " + (x * y));
		System.out.println("Division (X / Y): " + (x / y));
		System.out.println("Remainder (X % Y): " + (x % y));
	}
}









