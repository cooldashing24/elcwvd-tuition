class Char2ASCII2Demo2
{
	public static void main(String[] args)
	{
		char ch = 'H';
		System.out.println("Character: " + ch);
		//typecast
		System.out.println("ASCII value: " + (int)ch);
		
		ch = 'i';
		int x = (int)ch;
		System.out.println("Character: " + ch);
		//typecast
		System.out.println("ASCII value: " + x);
	}
}