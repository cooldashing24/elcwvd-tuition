// WAP to calculate sum and average of 3 numbers

class SumAvg3NosDemo
{
	public static void main(String[] args)
	{
		double x = 45.8, y = 35.7, z = 62.44;
		
		System.out.println("X: " + x);
		System.out.println("Y: " + y);
		System.out.println("Z: " + z);
		
		double sum = x + y + z;
		System.out.println("Sum of x, y & z: " + sum);
		System.out.println("Average: " + (sum / 3));
		
		
	}
	
}








