class QuoteDemo
{
	public static void main(String[] args)
	{
		String famousPerson = "Albert Einstein";
		String quote = "If you can't explain it to a six year old, you don't understand it yourself.";
		
		System.out.println(famousPerson + " once said, " + quote );
		System.out.println(famousPerson + " once said, \"" + quote + "\"");
	}
}